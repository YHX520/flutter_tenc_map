class Location {
  double latitude;

  double longitude;

  String name;

  String address;

  double altitude;

  double getLatitude() {
    return latitude;
  }

  double getLongitude() {
    return longitude;
  }

  double getAltitude() {
    return altitude;
  }

  String getName() {
    return name;
  }

  String getAddress() {
    return address;
  }
}
